/*
* Variable a , b
*/
/*
* let a contain 20
* b containt 7
*/ 
$a = 20;
$b = 7;

/*
*  Add two variable & echo
*  output: 27 
*/
echo $a+$b;
echo '<br/>';
/*
*  subtraction two variable
*  output: 13 
*/
echo $a-$b;

echo '<br/>';
/*
*  multply two variable
*  output: 140 
*/
echo $a*$b;

echo '<br/>';
/*
*  division two variable
*  output: 2.857142857.. 
*/
echo $a/$b;

echo '<br/>';
/*
*  modular two variable 
*  output: 6
*/
echo $a%$b;

